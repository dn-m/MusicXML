/// The backward-forward type is used to specify repeat directions. The start of the repeat has a
/// forward direction while the end of the repeat has a backward direction.
public enum BackwardForward: String {
    case backward
    case forward
}

extension BackwardForward: Equatable {}
extension BackwardForward: Codable {}
